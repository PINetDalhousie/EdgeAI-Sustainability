import tensorflow as tf
from tensorflow.keras.applications import ResNet50
from tensorflow.keras.optimizers import Adam
from tensorflow.keras.callbacks import ModelCheckpoint
from tensorflow.keras.datasets import mnist
from tensorflow.keras.utils import to_categorical

# Enable mixed precision training
from tensorflow.keras import mixed_precision
policy = mixed_precision.Policy('mixed_float16')
mixed_precision.set_global_policy(policy)

# Define constants
IMG_SIZE = 224
BATCH_SIZE = 32
EPOCHS = 5
MODEL_SAVE_PATH = 'resnet50_mnist_quantized.h5'

# Load the MNIST dataset
(x_train, y_train), (x_test, y_test) = mnist.load_data()

# Preprocess the datasets
x_train = tf.image.resize(x_train, [IMG_SIZE, IMG_SIZE])
x_train = tf.repeat(x_train, 3, -1)
x_train = tf.expand_dims(x_train, axis=0)
x_train = tf.repeat(x_train, len(y_train), axis=0)
x_train = x_train / 255.0

x_test = tf.image.resize(x_test, [IMG_SIZE, IMG_SIZE])
x_test = tf.repeat(x_test, 3, -1)
x_test = tf.expand_dims(x_test, axis=0)
x_test = tf.repeat(x_test, len(y_test), axis=0)
x_test = x_test / 255.0

y_train = to_categorical(y_train, 10)
y_test = to_categorical(y_test, 10)

# Load the ResNet50 model with pre-trained ImageNet weights
model = ResNet50(weights='imagenet', include_top=True, classes=1000)

# Ensure the model uses the policy by wrapping the optimizer with a LossScaleOptimizer
opt = Adam()
opt = mixed_precision.LossScaleOptimizer(opt)

# Compile the model
model.compile(optimizer=opt, loss='categorical_crossentropy', metrics=['accuracy'])

# Define a checkpoint callback
checkpoint = ModelCheckpoint(MODEL_SAVE_PATH, monitor='val_loss', save_best_only=True, verbose=1)

# Train the model
model.fit(
    x_train, y_train,
    validation_data=(x_test, y_test),
    epochs=EPOCHS,
    callbacks=[checkpoint]
)

