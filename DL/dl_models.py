BATCH_SIZE = 32
NUM_CLASSES = info.features['label'].num_classes

def preprocess_image(image, label):
    image = tf.image.resize(image, (IMG_SIZE, IMG_SIZE))
    image = tf.keras.applications.resnet50.preprocess_input(image)
    return image, label

